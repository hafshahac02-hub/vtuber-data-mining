def cari_kolom(df, keywords):
    for col in df.columns:
        col_lower = str(col).lower()
        if any(k in col_lower for k in keywords):
            return col
    cols_obj = [c for c in df.columns if df[c].dtype == 'object']
    if cols_obj:
        return cols_obj[0]
    return df.columns[0]

