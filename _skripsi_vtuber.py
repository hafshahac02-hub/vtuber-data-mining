# 4. FUNGSI ANALISIS LDA FLEKSIBEL
def run_lda(df_subset, label_nama, num_topics=5):
    if len(df_subset) == 0:
        print(f"⚠️ Data untuk {label_nama} kosong, LDA dilewati.\n")
        return

    print("=" * 50)
    print(f"ANALISIS LDA CHAT {label_nama.upper()} ({len(df_subset)} BARIS)")
    print("=" * 50)

    tokens = [str(text).split() for text in df_subset[col_teks_master].fillna('') if str(text).strip()]
    dict_lda = corpora.Dictionary(tokens)
    dict_lda.filter_extremes(no_below=3, no_above=0.5)

    if len(dict_lda) == 0:
        print("⚠️ Kata kunci terlalu sedikit setelah filter, LDA dilewati.\n")
        return

    corpus = [dict_lda.doc2bow(text) for text in tokens]

    lda = LdaModel(corpus=corpus, id2word=dict_lda, num_topics=num_topics, random_state=42, passes=10, alpha='auto')
    coherence = CoherenceModel(model=lda, texts=tokens, dictionary=dict_lda, coherence='c_v')
    score = coherence.get_coherence()

    print(f"Coherence Score (c_v) {label_nama}: {score:.4f}\n")
    for idx, topic in lda.print_topics(num_topics=num_topics, num_words=6):
        print(f"  * Topik #{idx + 1}: {topic}")
    print("\n")

