# 2. Kamus Normalisasi Slang & Jargon VTuber Indonesia
kamus_slang = {
    "ga": "tidak", "gak": "tidak", "gk": "tidak", "ndak": "tidak", "ngga": "tidak",
    "udh": "sudah", "dlu": "dulu", "jg": "juga", "dgn": "dengan",
    "kalo": "kalau", "bgt": "banget", "bangett": "banget", "bgtu": "begitu",
    "krn": "karena", "karna": "karena", "gw": "saya", "gua": "saya", "lu": "kamu",
    "stream": "siaran", "streaming": "siaran", "live": "siaran", "riging": "rigging",
    "wkwk": "tertawa", "wkwkwk": "tertawa", "awokwok": "tertawa", "ngakak": "tawa"
}

def normalisasi_teks(teks):
    words = teks.split()
    normalized_words = [kamus_slang.get(word, word) for word in words]
    return " ".join(normalized_words)

# 3. Fungsi Utama Pembersihan Teks (Menggabungkan Seluruh Prosedur Bab III)
def eksekusi_preprocessing(teks):
    if not isinstance(teks, str):
        return ""

    # a. Case Folding (Mengubah semua huruf menjadi kecil)
    teks = teks.lower()

    # b. Cleansing (Menghapus Mention, URL, Angka, dan Tanda Baca)
    teks = re.sub(r'@[A-Za-z0-9_\-]+', '', teks)  # Hapus @username
    teks = re.sub(r'http\S+|www\S+|https\S+', '', teks)  # Hapus Link URL
    teks = re.sub(r'\d+', '', teks)  # Hapus Angka
    teks = teks.translate(str.maketrans('', '', string.punctuation))  # Hapus Tanda Baca
    teks = re.sub(r'\s+', ' ', teks).strip()  # Hapus Spasi Berlebih

    # c. Normalisasi Slang
    teks = normalisasi_teks(teks)

    # d. Stemming Bahasa Indonesia (Mengubah ke Kata Dasar via Sastrawi)
    # DISINI PERBAIKANNYA: pakai .stem()
    teks = stemmer.stem(teks)

    return teks

