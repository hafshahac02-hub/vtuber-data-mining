def load_excel_or_csv(keyword):
    search_pattern = os.path.join(FOLDER_PATH, f"*{keyword}*")
    found = glob.glob(search_pattern)
    if not found:
        raise FileNotFoundError(f"File '{keyword}' tidak ditemukan di {FOLDER_PATH}")
    file_path = found[0]
    print(f"Membaca file: {os.path.basename(file_path)}")
    if file_path.endswith('.xlsx') or file_path.endswith('.xls'):
        df = pd.read_excel(file_path)
