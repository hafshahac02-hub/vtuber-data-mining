# BUKU INGATAN: Memori khusus biar Sastrawi gak mikir dua kali untuk kata yang sama
stem_cache = {}

def eksekusi_preprocessing_cepat(teks):
    if not isinstance(teks, str):
        return ""

    # a. Case Folding
    teks = teks.lower()

    # b. Cleansing
    teks = re.sub(r'@[A-Za-z0-9_\-]+', '', teks)
    teks = re.sub(r'http\S+|www\S+|https\S+', '', teks)
    teks = re.sub(r'\d+', '', teks)
    teks = teks.translate(str.maketrans('', '', string.punctuation))
    teks = re.sub(r'\s+', ' ', teks).strip()

    # c. Normalisasi Slang
    teks = normalisasi_teks(teks)

    # d. Fast Stemming Menggunakan Cache Kata (Rahasianya di sini!)
    words = teks.split()
    stemmed_words = []
    for word in words:
        if word not in stem_cache:
            # Cuma manggil rumus Sastrawi kalau katanya bener-bener baru belum pernah ketemu
            stem_cache[word] = stemmer.stem(word)
        stemmed_words.append(stem_cache[word])

    return " ".join(stemmed_words)

